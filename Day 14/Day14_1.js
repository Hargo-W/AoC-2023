const moveRockNorth = (field) => {
    for (let i = 1; i < field.length; i++) {
        for (let y = 0; y < field[0].length; y++) {
            if (field[i][y] === 'O' && field[i - 1][y] === '.') {
                field[i][y] = '.';
                field[i - 1][y] = 'O'
                moveRockNorth(field)
            }
        }
    }
    return field
}


const tiltedField = moveRockNorth(input)
